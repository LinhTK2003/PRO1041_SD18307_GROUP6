    private void loadTable(ArrayList<ChucVu> list) {
        dtm.setRowCount(0);
        for (ChucVu cv : list) {
            dtm.addRow(
                    new Object[]{
                        cv.getIdChucVu(),
                        cv.getTenChucVu()});                
        }
    }
    private void DetailTable(int index) {
        ChucVu cv = listcv.get(index);
        txtid.setText(cv.getIdChucVu() + "");
        txtChucVu.setText(cv.getTenChucVu());  
    }
    public ChucVu finData() {
        int IdChucVu = Integer.parseInt(txtid.getText());
        String TenChucVu = txtChucVu.getText();
        ChucVu chucVu = new ChucVu(IdChucVu, TenChucVu);
        return chucVu;
    }
    private boolean checkTrong() {
        if (txtid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "không để trống !");
            txtid.requestFocus();
            return false;
        }
        if (txtChucVu.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "không để trống !");
            txtChucVu.requestFocus();
            return false;
        }
        return true;
    }
    
    
